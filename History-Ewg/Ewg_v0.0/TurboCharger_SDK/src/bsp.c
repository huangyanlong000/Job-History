void Bsp_Init(void)
{

    /******************************************
     * @brief : Initialize clock
     * ***************************************/
    {
        CLOCK_DRV_Init(&clockMan1_InitConfig0);
    }

    /******************************************
     * @brief : Initialize GPIOA~D
     * ***************************************/
    {
        PINS_DRV_Init((0x00),&g_pin_mux_InitConfigArr0);
    }

    /******************************************
     * @brief : Initialize flexcan0
     * ***************************************/
    {
        FLEXCAN_DRV_Init((0x00),&flexcanState0,&flexcanInitConfig0);
    }
}
